package cn.slkj.taxi.entity;

public class TaxiCarApply {
	private int id;
	private String corpName;
	private String applicant;
	private String matter;
	private String opretaCertNum;
	private String oldPlateNum;
	private String newPlateNum;
	private String optTime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCorpName() {
		return corpName;
	}

	public void setCorpName(String corpName) {
		this.corpName = corpName;
	}

	public String getApplicant() {
		return applicant;
	}

	public void setApplicant(String applicant) {
		this.applicant = applicant;
	}

	public String getMatter() {
		return matter;
	}

	public void setMatter(String matter) {
		this.matter = matter;
	}

	public String getOpretaCertNum() {
		return opretaCertNum;
	}

	public void setOpretaCertNum(String opretaCertNum) {
		this.opretaCertNum = opretaCertNum;
	}

	public String getOldPlateNum() {
		return oldPlateNum;
	}

	public void setOldPlateNum(String oldPlateNum) {
		this.oldPlateNum = oldPlateNum;
	}

	public String getNewPlateNum() {
		return newPlateNum;
	}

	public void setNewPlateNum(String newPlateNum) {
		this.newPlateNum = newPlateNum;
	}

	public String getOptTime() {
		return optTime;
	}

	public void setOptTime(String optTime) {
		this.optTime = optTime;
	}

}
